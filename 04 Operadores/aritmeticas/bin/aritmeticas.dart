void main(List<String> arguments) {
  int a = 10;
  int b = 25;

  //suma
  print(a + b);

  //resta
  print(b - a);

  //multiplicación
  print(a * b);

  //división
  print(b / a);

  //incremento
  print(a);
  a++;
  print(a);

  //decremento
  print(a);
  a--;
  print(a);

  //division entera
  print(b ~/ a);

  //módulo
  print(b % a);

  //cambiar signo
  print(-a);
}
